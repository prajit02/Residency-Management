    public void addLogs(javax.swing.JTable table, String q) {
        try {
            Statement stmt = conn.createStatement();
            String query = q;
            ResultSet rs = stmt.executeQuery(query);

            DefaultTableModel tblModel = (DefaultTableModel) table.getModel();
            
            SimpleDateFormat sdfDate = new SimpleDateFormat("dd-MM-yyyy");
            sdfDate.setTimeZone(java.util.TimeZone.getTimeZone("IST"));

            SimpleDateFormat sdfTime = new SimpleDateFormat("hh:mm:ss a");
            sdfTime.setTimeZone(java.util.TimeZone.getTimeZone("IST"));
            
            if(!rs.isBeforeFirst()) {
                JOptionPane.showMessageDialog(this, "No record found!");
                return;
            }

            tblModel.setRowCount(0);
            while(rs.next()){
                String blNo = String.valueOf(rs.getString("vis_block"));
                String hoNo = String.valueOf(rs.getString("vis_house_no"));
                String visName = String.valueOf(rs.getString("vis_name"));
                String vehicleNo = String.valueOf(rs.getString("vehicle_no"));
                String visCont = String.valueOf(rs.getString("vis_contact"));
                String visEntry = String.valueOf(rs.getString("vis_entry"));
                
                Timestamp tstamp = Timestamp.valueOf(visEntry);
                
                String dateStr = sdfDate.format(tstamp);
                String timeStr = sdfTime.format(tstamp);

                String tbData[] = {blNo,hoNo,visName,vehicleNo,visCont,dateStr,timeStr};

                tblModel.addRow(tbData);
            }
        } catch(Exception e){
            System.out.print(e);
        }
    }
    
