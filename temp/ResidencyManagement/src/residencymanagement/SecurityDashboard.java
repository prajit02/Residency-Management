    private void jPanel1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MousePressed
        // TODO add your handling code here:   
        bNo = (String) cbBlockNo.getSelectedItem();
        hNo = txtHouseNo.getText();
        id = null;
        
        if(bNo.equals("")||hNo.equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter house no.");
            return;
        }
        
        
        
        try {
            Statement selectStmt = conn.createStatement();
            ResultSet rs = selectStmt.executeQuery("SELECT * FROM resident WHERE res_block='" +bNo+ "' AND res_house_no='" + hNo + "'");
            
            if(!rs.isBeforeFirst()) {
                jPanel2.setVisible(false);
                btnNext.setVisible(false);
                JOptionPane.showMessageDialog(this, "No record found!");
                return;
            }
            
            while(rs.next()) {
                lblResiName.setText(rs.getString(2));
                lblResiAddress.setText(rs.getString(3)+" "+rs.getString(4));
                id = rs.getString(1);
                
                jPanel2.setVisible(true);
                btnNext.setVisible(true);
            }
            
            ResultSet rs2 = selectStmt.executeQuery("SELECT * FROM resident_contact WHERE res_id="+id);
            while(rs2.next()) {
                lblResiContact.setText(lblResiContact.getText()+rs2.getString(2)+", ");
            }
        } catch (SQLException ex) {
            Logger.getLogger(SecurityDashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jPanel1MousePressed

