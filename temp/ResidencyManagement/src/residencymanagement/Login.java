    private void btn_admin_proceedMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_admin_proceedMouseClicked
        String username = tf_username.getText();
        String password = tf_password.getText();
        
        try{
            Statement stmt = conn.createStatement();
            String query = "SELECT * FROM admin WHERE ad_name="+"'"+username+"'"+" AND ad_pwd="+"'"+password+"'";
            ResultSet rs = stmt.executeQuery(query);
            
            if(rs.next()){
                dispose();
                Admin_Panel adPanel = new Admin_Panel();
                adPanel.setVisible(true);
                adPanel.setAdminId(rs.getString("ad_name"));
                adPanel.setSocName(rs.getString("soc_name"));
            }else{
                JOptionPane.showMessageDialog(null, "Incorrect Username or Password");
                return;
            }
            
        }
        catch(Exception e){
            System.out.print(e.getMessage().toString());
        }
    }//GEN-LAST:event_btn_admin_proceedMouseClicked

