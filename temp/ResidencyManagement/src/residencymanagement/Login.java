    private void btn_login_proceedMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_login_proceedMouseClicked

        String username = tf_username1.getText();
        String password = tf_password1.getText();
        
        try{
            Statement stmt = conn.createStatement();
            String query = "SELECT * FROM security WHERE sec_name="+"'"+username+"'"+" AND sec_pwd="+"'"+password+"'";
            ResultSet rs = stmt.executeQuery(query);
            
            if(rs.next()){
                dispose();
                SecurityDashboard secDash = new SecurityDashboard();
                secDash.setSecurityId(rs.getString("sec_name"));
                secDash.setSocName(rs.getString("soc_name"));
                secDash.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Incorrect Username or Password");
                return;
            }
            
        }
        catch(Exception e){
            System.out.print(e.getMessage().toString());
        }
        
    }//GEN-LAST:event_btn_login_proceedMouseClicked

