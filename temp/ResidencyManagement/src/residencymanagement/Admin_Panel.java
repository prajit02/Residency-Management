    public void addLogs(javax.swing.JTable table, String q) {
        try {
            Statement stmt = conn.createStatement();
            String query = q;
            ResultSet rs = stmt.executeQuery(query);

            DefaultTableModel tblModel = (DefaultTableModel) table.getModel();
            
            tblModel.setRowCount(0);
            while(rs.next()){
                String blNo = String.valueOf(rs.getString("m_block"));
                String hoNo = String.valueOf(rs.getString("m_house_no"));
                String mName = String.valueOf(rs.getString("m_name"));
                String mCost = String.valueOf(rs.getString("m_charge"));
                String mStatus = String.valueOf(rs.getString("m_status"));
                Boolean b = false;
                
                
                if(mStatus.equals("true")) {
                    System.out.println(mStatus);
                    b=true;
                }

                Object tbData[] = {blNo,hoNo,mName,mCost,b};

                tblModel.addRow(tbData);
            }
        } catch(Exception e){
            System.out.print(e);
        }
    }
    
