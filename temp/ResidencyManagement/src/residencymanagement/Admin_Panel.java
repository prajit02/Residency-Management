                JOptionPane.showMessageDialog(null, "Record Inserted Successfully!");
                
                String query2 = "SELECT * FROM resident WHERE res_block="+"'"+resi_block+"'"+" AND res_house_no="+"'"+resi_houseno+"'";
                ResultSet rs2 = stmt.executeQuery(query2);
                String resId = null;
                
                while(rs2.next()) {
                    resId = rs2.getString("res_id");
                }
                
                for(String s: arrContact) {
                    if(!s.trim().equals("")) {
                        String query3 = "INSERT INTO resident_contact values('"+resId+"','"+s.trim()+"')";
                        stmt.executeUpdate(query3);
                    }
                }
